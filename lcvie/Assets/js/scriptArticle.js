$(document).ready(function(){
    console.log($('#content-modif')[0].innerHTML);
    const cas1 = "<i class=\"fas fa-angle-double-down\" aria-hidden=\"true\"></i> Lire la suite";
    const cas2 = "<i class=\"fas fa-angle-double-up\"></i> Résumé";

    $('#content-modif').on('click',function(e){
        e.preventDefault();

        if($('#content-modif')[0].innerHTML = cas1){

            $('#content-modif')[0].innerHTML = cas2;
        }else{
            $('#content-modif')[0].innerHTML = cas1;

        }

    })

})